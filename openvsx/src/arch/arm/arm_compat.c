double log2(double x) {
  int n = 0;
  uint32_t v = (uint32_t) x;
  if (v & 0xffff0000) {
    v >>= 16;
    n += 16;
  }
  if (v & 0xff00) {
    v >>= 8;
    n += 8;
  }
  n += log2_vals[(uint8_t)v];

  return (double) n;
}

