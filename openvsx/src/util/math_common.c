int math_log2_32(uint32_t ui32) {

  int rc = 0;

  if ((ui32 & 0xffff0000)) {
    ui32 >>= 16;
    rc += 16;
  }
  if ((ui32 & 0xff00)) {
    ui32 >>= 8;
    rc += 8;
  }
  rc += s_log2_vals[ui32];

  return rc;
}
